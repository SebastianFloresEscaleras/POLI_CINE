    public void crearNuevaFuncion(){
        Pelicula pelicula1 = new Pelicula("","","");
        Sala sala1 = new  Sala("");
        Horario horario1 = new Horario("");
        
        for(Pelicula a: cinegestor1.retornarListaPeliculas()){
            
            if(jList1.getSelectedValue().equals(a.retornarNombre())){
                pelicula1 = a;
            }
            
        }
        
        for(Sala a: cinegestor1.retornarListaSala()){
            if(jList2.getSelectedValue().equals(a.retornarId())){
                sala1 = a;
            }
        }
        
        for(Horario a: cinegestor1.retornarListaHorarios()){
            if(jList3.getSelectedValue().equals(a.retornarHorario())){
                horario1 = a;
            }
        }
        
        cinegestor1.retornarListaFunciones().add(new Funcion(pelicula1, sala1, horario1));
        
        System.out.println("Probando si existe la nueva funcion");
        for(Funcion a: cinegestor1.retornarListaFunciones()){
            System.out.println(a.retornarPelicula().retornarNombre());
        }
    }
    
