    public void llenarListas(){
        
        for(Pelicula a: this.cinegestor1.retornarListaPeliculas()){
        listaLogica1.addElement(a.retornarNombre());        
        }
        
        jList1.setModel(listaLogica1);
        
        
        for(Sala ar: this.cinegestor1.retornarListaSala()){
            
        listaLogica2.addElement(ar.retornarId());  
        System.out.println(ar.retornarId());
        }
        
        jList2.setModel(listaLogica2);
        
        
        for(Horario ar: this.cinegestor1.retornarListaHorarios()){
            listaLogica3.addElement(ar.retornarHorario());
            
        }
        
        jList3.setModel(listaLogica3);
        
    
    }
    
    
    
    
    

